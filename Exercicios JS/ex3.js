const valor = 30;

const respostaA = (valor === 20) ? "SIM" : "NÃO";
const respostaB = (valor !== 20) ? "SIM" : "NÃO";
const respostaC = (valor < 50) ? "SIM" : "NÃO";
const respostaD = ((valor - 20) === 0) ? "SIM" : "NÃO";

console.log("a. O valor é igual a 20? Resposta:", respostaA);
console.log("b. O valor é diferente de 20? Resposta:", respostaB);
console.log("c. O valor é menor que 50? Resposta:", respostaC);
console.log("d. O valor menos 20 é igual a 0? Resposta:", respostaD);
